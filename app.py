import random
from datetime import datetime

# Simulated trend data (Real mein API se fetch hoga)
trends_data = {
    "fashion": {
        "trending_keywords": ["#FashionTrends", "#StyleInspo", "#OOTD", "trendy", "chic"],
        "popular_niches": ["sneakers", "dresses", "jackets"]
    },
    "tech": {
        "trending_keywords": ["#TechNews", "#GadgetLovers", "#Innovation", "smart", "future"],
        "popular_niches": ["smartphone", "laptop", "earbuds"]
    },
    "food": {
        "trending_keywords": ["#Foodie", "#Yummy", "#HealthyEats", "delicious", "tasty"],
        "popular_niches": ["pizza", "smoothie", "cake"]
    }
}

class InstaTrendOptimizer:
    def __init__(self, niche, product_name):
        self.niche = niche.lower()
        self.product_name = product_name.lower()
        self.current_date = datetime.now().strftime("%Y-%m-%d")

    def fetch_trends(self):
        # Simulated trend fetching (Real mein API ya scraping ka use karo)
        if self.niche in trends_data:
            return trends_data[self.niche]["trending_keywords"]
        else:
            return ["#Trending", "#Viral", "#Explore", "new", "hot"]  # Default trends

    def generate_caption(self, trends):
        # Caption with product and trend integration
        base = f"Check out this amazing {self.product_name}! "
        trend_word = random.choice(trends).replace("#", "")
        return f"{base}Perfect for {trend_word} vibes this {self.current_date}. {random.choice(['Love it!', 'Must-have!', 'Wow!'])}"

    def generate_hashtags(self, trends):
        # Hashtags: Niche + Product + Trends
        niche_tags = [f"#{self.niche}", f"#{self.niche}{self.product_name}"]
        product_tags = [f"#{self.product_name}", f"#{self.product_name}Love"]
        return niche_tags + product_tags + trends[:3]  # Top 3 trends

    def generate_seo_keywords(self, trends):
        # SEO keywords: Product + Niche + Trend-related
        base_keywords = [self.product_name, self.niche]
        trend_keywords = [word.replace("#", "") for word in trends[:2]]
        return base_keywords + trend_keywords + ["2025", "latest"]

    def optimize(self):
        trends = self.fetch_trends()
        output = {
            "caption": self.generate_caption(trends),
            "hashtags": self.generate_hashtags(trends),
            "seo_keywords": self.generate_seo_keywords(trends)
        }
        return output

